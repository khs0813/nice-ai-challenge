CREATE TABLE BM_QUERY_COMPARISON (
    COMPARISON_ID NUMBER(19) NOT NULL,
    COMPARISON_NAME VARCHAR2(200) NOT NULL,
    DESCRIPTION VARCHAR2(1000),
    SYBASE_QUERY CLOB NOT NULL,
    ORACLE_QUERY CLOB NOT NULL,
    COMPARE_MODE VARCHAR2(20) DEFAULT 'EXACT' NOT NULL,
    ENABLED_YN CHAR(1) DEFAULT 'Y' NOT NULL,
    SCHEDULE_ENABLED_YN CHAR(1) DEFAULT 'N' NOT NULL,
    CRON_EXPRESSION VARCHAR2(100),
    NOTIFY_ENABLED_YN CHAR(1) DEFAULT 'Y' NOT NULL,
    NOTIFY_ON_SUCCESS_YN CHAR(1) DEFAULT 'N' NOT NULL,
    NOTIFY_RECIPIENTS VARCHAR2(1000),
    AI_SQL_REVIEW_YN CHAR(1) DEFAULT 'N' NOT NULL,
    AI_ANALYSIS_ENABLED_YN CHAR(1) DEFAULT 'Y' NOT NULL,
    AI_SQL_REVIEW_STATUS VARCHAR2(20),
    AI_SQL_REVIEW_SUMMARY CLOB,
    CREATED_BY VARCHAR2(100),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY VARCHAR2(100),
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_TEST_QUERY_COMPARISON PRIMARY KEY (COMPARISON_ID),
    CONSTRAINT CK_TEST_QC_COMPARE_MODE CHECK (COMPARE_MODE IN ('EXACT', 'HASH')),
    CONSTRAINT CK_TEST_QC_ENABLED_YN CHECK (ENABLED_YN IN ('Y', 'N')),
