    CONSTRAINT CK_TEST_QC_NOTIFY_YN CHECK (NOTIFY_ENABLED_YN IN ('Y', 'N')),
    CONSTRAINT CK_TEST_QC_NOTIFY_SUCCESS_YN CHECK (NOTIFY_ON_SUCCESS_YN IN ('Y', 'N')),
    CONSTRAINT CK_TEST_QC_AI_REVIEW_YN CHECK (AI_SQL_REVIEW_YN IN ('Y', 'N')),
    CONSTRAINT CK_TEST_QC_AI_ANALYSIS_YN CHECK (AI_ANALYSIS_ENABLED_YN IN ('Y', 'N'))
);

CREATE TABLE BM_QUERY_COMPARISON_RESULT (
    RESULT_ID NUMBER(19) NOT NULL,
    COMPARISON_ID NUMBER(19) NOT NULL,
    REQUESTED_BY VARCHAR2(100),
